package com.sankar;

public class Primitives { //primitives are the ones that cannot be broken into parts
    public static void main(String[] args) {
        int rollno =31; //max 4 bytes can be stored
        char letter = 's'; //max 2 bytes can be stored
        float marks = 99.54f;//max 4 bytes can be stored
        double largeDecimalNumbers = 4444455335.65643253; //max 8 bytes can be stored
        long largeInteger = 348275629030L; //max 8 bytes can be stored
        boolean check = false; // single byte of memory

        //String is not a primitive datatype
        String newstr = "My name is Linux"; //strings can be broken into parts for further processing

    }
}
